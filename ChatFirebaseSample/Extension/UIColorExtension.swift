import UIKit

extension UIColor {
    public class var lightSalmonPink : UIColor {
        return UIColor(red: 248/255, green: 153/255, blue: 157/255, alpha: 1)
    }
    
    public class var turquoise : UIColor {
        return UIColor(red: 35/255, green: 240/255, blue: 199/255, alpha: 1)
    }
    
    public class var lightPurpleUbe : UIColor {
        return UIColor(red: 125/255, green: 122/255, blue: 188/255, alpha: 1)
    }
    
    public class var darkPurpleLiberty : UIColor {
        return UIColor(red: 100/255, green: 87/255, blue: 166/255, alpha: 1)
    }
    
    public class var yellowGargoyleGas : UIColor {
        return UIColor(red: 255/255, green: 227/255, blue: 71/255, alpha: 1)
    }
}
